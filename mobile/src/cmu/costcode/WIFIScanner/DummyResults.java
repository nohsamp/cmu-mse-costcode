	public static List<AccessPoint> parseDummyResult(int count) {
		List<Dummy> scanResult = makeScanResults(count);

		Comparator<Dummy> comparator = new Comparator<Dummy>() {
			@Override
			public int compare(Dummy s1, Dummy s2) {
				return (s1.getSsid().compareTo(s2.getSsid()));
			}
		};
		Collections.sort(scanResult, comparator);

		int rssi = 0;// TODO: remove rssi2

		int tCount = 0;

		Dummy oldResult = null, newResult = null;

		for (int i = 0; i < scanResult.size(); i++) {
			newResult = scanResult.get(i);

			if (i > 0
					&& oldResult.getSsid().compareTo(newResult.getSsid()) != 0) {
				// if noiseFilterFlag is true and tCount is less than scanCount,
				// drop info from AP
				if (tCount == count) {
					addDummyAP(oldResult, i, rssi / tCount);
				}
				rssi = newResult.getRssi();
				tCount = 1;
			} else {
				rssi += newResult.getRssi();
				tCount++;
			}

			oldResult = newResult;
			if (i == scanResult.size() - 1) {
				// if noiseFilterFlag is true and tCount is less than scanCount,
				// drop info from AP
				if (tCount == count) {
					addDummyAP(oldResult, i, rssi / tCount);
				}
			}
		}

		return apList;
	}

